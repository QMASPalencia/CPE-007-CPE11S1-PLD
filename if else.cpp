#include <iostream>

using namespace std;
int main()
{
int x,y;

cout<<"Please input 2 integers on the following"<<endl;
cout<<"Please input your x value: ";cin>>x;
cout<<"Please input your y value: ";cin>>y;

if(x>y)
{
	
	cout<<"x is greater than y";
	
}
else if(x<y)
{
	
	cout<<"x is less than y";
	
}

else
{
	
	cout<<"x is equal to y";
		
}

return 0;
}
